//Dado un string "Hola, cómo estás?", conviértelo a mayúsculas y elimina las comas.

//DEFINIMOS UNA CADENA COMO CONSTANT YA QUE NO SE MODIFICARÁ
const cadena = "Hola, cómo estás?";

//APLICAMOS EL MÉTODO toUpperCase() PARA CONVERTIR LA CADENA A MAYÚSCULAS Y LO GUARDAMOS EN UNA VARIABLE NUEVA
//PARA FINES DE LA EXPLICACIÓN.
let mayusculas = cadena.toUpperCase();

//FINALMENTE USAMOS EL MÉTODO .replace() PARA ELIMINAR LAS COMAS DE LA CADENA Y LO GUARDAMOS EN OTRA VARIABLE
//EN ESTE MÉTODO USAMOS UNA EXPRESIÓN REGULAR /,/g PARA INDICAR QUE QUEREMOS REEMPLAZAR LAS COMAS (/,) EN TODA LA CADENA (/g).
//Y LAS REMPLACE POR UNA CADENA VACÍA ('').
let sinComas = mayusculas.replace(/,/g, '');


//ADEMAS DEL GLOBAR (/g) PODEMOS USAR OTROS MODIFICADORES COMO /i (IGNORAR MAYÚSCULAS Y MINÚSCULAS) o /m (MULTILÍNEA).

//FINALMENTE IMPRIMIMOS LA CADENA CONVERTIDA
console.log(sinComas);