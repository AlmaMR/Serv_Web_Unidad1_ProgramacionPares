// Operaciones matemáticas
// Declara dos variables con números y realiza las operaciones básicas:
// suma, resta, multiplicación y división. Imprime los resultados.

// Declaramos dos variables y les asignamos un numero a cada una
let num1 = 73;
let num2 = 23;

// Las imprimimos en consola para tener la referencia
console.log("Numero 1:", num1 );
console.log("Numero 2:", num2);

// Realizamos cada una de las operaciones solicitadas y guardamos el
// resultado de cada una en una variable diferente
let suma = num1 + num2;
let resta = num1 - num2;
let multi = num1 * num2;
let div = num1 / num2;

// Imprimimos en consola el resultado de cada una de las operaciones
console.log("Suma: ", suma);
console.log("Resta: ", resta);
console.log("Multiplicacion: ", multi);
console.log("Division: ", div);