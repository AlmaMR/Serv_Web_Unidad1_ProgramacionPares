// Condicionales (if-else)
// Escribe un programa que pregunte al usuario su edad y
// le diga si es mayor de edad (18 años o más) o no.

// Declaramos una variable que recibira y guardara un numero
let edad = prompt("Ingresa tu edad: ");

// Usamos el condicional if con la condición
// edad >= 18, para comprobar si es mayor de edad o no
if (edad >= 18) {
    console.log("Eres Mayor de Edad!!");
    // Como se cumple la condición imprime en consola que es mayor de edad
} else {
    console.log("No Eres Mayor de Edad!!");
    // Como no se cumple la condición imprime en consola que no es mayor de edad
}